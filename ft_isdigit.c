#include "ft_push_swap.h"

void	ft_isdigit(char *join)
{
	int	i;

	i = 0;
	while (join[i] != '\0')
	{
		if (!((join[i] >= '0' && join[i] <= '9')
				|| ((join[i] == 45 && join[i + 1] != 45)
					|| (join[i] == 43 && join[i + 1] != 43))
				|| (join[i] >= 9 && join[i] <= 13)
				|| join[i] == 32))
		{
			write(2, "ERROR\n", 6);
			free(join);
			exit(1);
		}
		i++;
	}
}
