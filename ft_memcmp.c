#include "ft_push_swap.h"

int	ft_memcmp(int *s1, int n)
{
	int	i;
	int	j;

	i = 0;
	if (!s1)
		return (0);
	while (i < n)
	{
		j = i + 1;
		while (j < n)
		{
			if (s1[i] == s1[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
